public class Main {
    public static void main(String[] args) {
        System.out.println("Hello, world!");
    }


    public static void print(String str) {
        System.out.println(str);
    }


    public static void print(String str, int num) {
        for (int i = 0; i < num; i++) {
            System.out.println(str);
        }
    }
}
